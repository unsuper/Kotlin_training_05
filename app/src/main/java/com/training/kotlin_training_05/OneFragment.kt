package com.training.kotlin_training_05

import android.content.Context
import android.os.Bundle
import android.util.Log
import androidx.fragment.app.Fragment
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.core.os.bundleOf
import androidx.navigation.Navigation.findNavController
import com.training.kotlin_training_05.databinding.FragmentOneBinding
import androidx.navigation.findNavController
import androidx.navigation.fragment.findNavController

class OneFragment : Fragment() {

    private lateinit var fragmentOneBinding: FragmentOneBinding
    private val TAG = "ONE_FRAGMENT"

    override fun onAttach(context: Context) {
        super.onAttach(context)
        Log.d(TAG, "onAttach: ")
    }

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        Log.d(TAG, "onCreate: - Get arguments")
    }

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        return inflater.inflate(R.layout.fragment_one, container, false)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        val binding = FragmentOneBinding.bind(view)
        fragmentOneBinding = binding
        fragmentOneBinding.editTextOneFragment.setText("First text assign to fragment editText")
        fragmentOneBinding.buttonSendName.setOnClickListener {

            val name = fragmentOneBinding.editTextOneFragment.text.toString()
            if (name.isEmpty()) {
                fragmentOneBinding.editTextOneFragment.error = "Please enter a name"
            } else {
                val bundle = bundleOf("name" to name)
                view.findNavController().navigate(R.id.action_oneFragment_to_twoFragment, bundle)
            }
        }
    }

    override fun onActivityCreated(savedInstanceState: Bundle?) {
        super.onActivityCreated(savedInstanceState)
        Log.d(TAG, "onActivityCreated: ")
    }

    override fun onStart() {
        super.onStart()
        Log.d(TAG, "onStart: ")
    }

    override fun onResume() {
        super.onResume()
        Log.d(TAG, "onResume: ")
    }

    override fun onPause() {
        super.onPause()
        Log.d(TAG, "onPause: ")
    }

    override fun onStop() {
        super.onStop()
        Log.d(TAG, "onStop: ")
    }

    override fun onDestroyView() {
        super.onDestroyView()
        Log.d(TAG, "onDestroyView: ")
    }

    override fun onDestroy() {
        super.onDestroy()
        Log.d(TAG, "onDestroy: ")
    }

    override fun onDetach() {
        super.onDetach()
        Log.d(TAG, "onDetach: ")
    }
}